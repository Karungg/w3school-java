public class JavaDataTypesNumbers {
    public static void main(String[] args) {
        // Primitive numbers data types

        // Byte can store -128 to 127
        byte myByte = 127;
        System.out.println(myByte);

        // short can store -32768 to 32767
        short myShort = 5000;
        System.out.println(myShort);

        // int can store -2147483648 to 2147483647
        int myInt = 2147483647;
        System.out.println(myInt);

        // Long can store -9223372036854775808 to 9223372036854775807
        long myLong = 9223372036854775807L;
        System.out.println(myLong);

        float myFloat = 3.85f;
        System.out.println(myFloat);

        double myDouble = 4.00d;
        System.out.println(myDouble);

        float f1 = 35e3f;
        double d1 = 12E4d;
        System.out.println(f1);
        System.out.println(d1);
    }
}
